package cpsc4150.projects.journal.tabs;

/*
    The direction your finger swipe.

    Legend:
        DEFAULT - default direction tabs can be swiped in -- to the left or right.
        LEFT - only allow swipes to the left
        RIGHT - only allow swipes to the right
        NONE - tab is unswipeable
 */
public enum SwipeDirection {
    DEFAULT, LEFT, RIGHT, NONE
}
